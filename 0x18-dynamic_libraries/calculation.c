#include <stdio.h>

/**
 * add - addition of two integers
 * @a: the first integer
 * @b: second integer
 * Return: addition
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two integers
 * @a: first integer
 * @b: second integer
 * Return: subtraction
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two integers
 * @a: first integer
 * @b: second integer
 * Return: multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide two integers
 * @a: first integers
 * @b: second integers
 * Return: division
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - mod of two integers
 * @a: first integer
 * @b: second integers
 * Return: final answer
 */
int mod(int a, int b)
{
	return (a & b);
}
